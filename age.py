# Single Value Container

# Container Creation
age = 20

# Read/Print Statement
# type -> class
# id   -> HashCode
print("Age:", age, type(age), id(age))
print(age, type(age), hex(id(age)))
print(age, type(age), oct(id(age)))
print(age, type(age), bin(id(age)))

